Observações
1. Alemanha
A reunificação alemã provocou o desaparecimento da RDA devido à integração, tornando a vaga alemã da RFA em 3 de outubro de 1990.

2. Birmânia
A antiga Birmânia mudou seu nome em 1989 para Mianmar.

3. China e Taiwan
A República da China foi um dos membros fundadores das Nações Unidas em 1945. Em 1949, com o fim da guerra civil chinesa, o governo da República da China
do partido Kuomintang de Chiang Kai-shek refugiou-se em Taiwan, passando o controle da parte continental do país para a nova República Popular da China,
com um governo comunista.

O assento chinês na ONU foi ocupado pela República da China até o dia 25 de outubro de 1971, data da resolução 2758 da Assembleia Geral das Nações Unidas,
que substitui Taiwan pela República Popular da China em todas as instâncias da ONU, inclusive no Conselho de Segurança.

Desde 1991, a República da China (ROC) tem feito repetidamente petições para reinclusão na ONU, como representação da população de Taiwan, em vez de todos
na China, usando as designações “República da China em Taiwan”, “República da China (Taiwan)” ou apenas “Taiwan . No entanto, todas as tentativas têm sido
negadas, porque o pedido não consegue votos suficientes para a agenda ou porque foi rejeitada pela ONU, devido à oposição da República Popular da China (PRC).

Em julho de 2007, o atual presidente de Taiwan, Chen Shui-bian apresentou a 15.ª solicitação para se unir às Nações Unidas, sob o nome de Taiwan, mas que
foi rejeitada pelo Escritório de Assuntos Jurídicos da ONU, citando a Resolução 2758 da Assembleia Geral.

Em resposta à rejeição da ONU, o governo da ROC tem esclarecido que Taiwan não é parte da China, e que a Resolução 2758 não toca a questão da representação
de Taiwan na ONU, e portanto não impede a participação de Taiwan como uma nação soberana independente.

Por outro lado, o governo da PRC, que afirma ser Taiwan parte integrante da China, elogia que a decisão das Nações Unidas “foi tomada em acordo com o
Estatuto da ONU e da Resolução 2758 da Assembleia Geral.

Atualmente tentativas de incluir novamente a República da China(Taiwan) no sistema das Nações Unidas como representante dos "23 milhões de habitantes" de
Taiwan têm sido infrutíferas.

4. Egito
A República Árabe Unida foi membro de 21 de fevereiro de 1958 até 2 de setembro de 1971, quando o Egito mudou de nome para República Arábe do Egito.

5. Iêmen (Iémen)
A República Árabe do Iêmen ingressou na ONU em 30 de setembro de 1947, enquanto a República Democrática Popular do Iêmen ingressou em 14 de dezembro em 1967. Os dois países se uniram em 1990 e o status de seus assentos foram transferidos para o Iêmen.

6. Indonésia, Malásia e Cingapura (Singapura)
A Federação Malaia ingressou à ONU em 1957. Em 16 de setembro de 1963, formou-se a Federação Malásia, unindo o antigo território Malaio a Singapura,
Bornéu do Norte (Sabah) e Sarawak. Singapura tornou-se um estado independente em 9 de agosto de 1965.

A Indonésia, com pretensões territoriais sobre a Malásia, retirou-se temporariamente da ONU em 1965 como protesto ao ingresso deste país no Conselho de
Segurança. Após revoltas populares e tentativas de um golpe militar no mesmo ano, Sukarno perdeu o poder para o militar Suharto, que reintegrou o país na ONU em 1966.

7. Iugoslávia (Jugoslávia)
A Iugoslávia foi a primeira a ratificar a Carta das Nações Unidas, em 19 de outubro de 1945, e foi membro efetivo até 10 de novembro de 2000. Os novos
